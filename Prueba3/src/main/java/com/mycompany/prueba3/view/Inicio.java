     public void filtro() {
        int columnaGenero = 5;
        int columnaAnho = 3;
        int anhoFiltro = 1900;
        int anhoFiltro2 = 2023;
        
        if(!jTextFieldAnhoFiltro1.getText().isEmpty()){
            anhoFiltro = Integer.parseInt(jTextFieldAnhoFiltro1.getText());
        }
        if(!jTextFieldAnhoFiltro2.getText().isEmpty()){
            anhoFiltro2 = Integer.parseInt(jTextFieldAnhoFiltro2.getText());
        }
        
        
        List<RowFilter<Object , Object>> filters = new ArrayList<RowFilter<Object , Object>>();
        filters.add(RowFilter.regexFilter(jTextFieldFiltro.getText(), columnaGenero));
        filters.add(RowFilter.numberFilter(RowFilter.ComparisonType.AFTER, anhoFiltro -1, columnaAnho));
        filters.add(RowFilter.numberFilter(RowFilter.ComparisonType.BEFORE, anhoFiltro2 +1, columnaAnho));
        
        
        this.trsfFiltro.setRowFilter(RowFilter.andFilter(filters));
     }
     
    public void cargarTabla() throws SQLException{
        PeliculaController controller = new PeliculaController();
        
        ArrayList<MovieDAO> listaPeliculaDAO = new ArrayList<MovieDAO>();
        
        listaPeliculaDAO = controller.ListaPeliculaController(this.conector.getConn());
        
        DefaultTableModel modelo = (DefaultTableModel) this.jTableLista.getModel();
        modelo.setRowCount(0);
        
        for (int i = 0; i < listaPeliculaDAO.size(); i++){
            Object[] objeto = {
                listaPeliculaDAO.get(i).getId(),
                listaPeliculaDAO.get(i).getTitulo(),
                listaPeliculaDAO.get(i).getDirector(),
                listaPeliculaDAO.get(i).getAnho(),
                listaPeliculaDAO.get(i).getDuracion(),
                listaPeliculaDAO.get(i).getGenero()
            };
            modelo.addRow(objeto);
        }
    }

