    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        
        int anho = Integer.parseInt(jTextFieldAnho.getText());
        
        if(this.jTextFieldTitulo.getText().isEmpty() || this.jTextFieldDirector.getText().isEmpty() || this.jTextFieldAnho.getText().isEmpty() || 
                    this.jTextFieldDuracion.getText().isEmpty() || this.jTextFieldGenero.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "faltan campos por completar");
        }else{
            if(anho<1900 || anho>2023){
            JOptionPane.showMessageDialog(this, "ingrese año mayor a 1900 o menor a 2023");
        }else{
            MovieDAO peliculaNueva = new MovieDAO();
            peliculaNueva.setTitulo(jTextFieldTitulo.getText().trim());
            peliculaNueva.setDirector(jTextFieldDirector.getText().trim());
            peliculaNueva.setAnho(Integer.valueOf(jTextFieldAnho.getText().trim()));
            peliculaNueva.setDuracion(Integer.valueOf(jTextFieldDuracion.getText().trim()));
            peliculaNueva.setGenero(jTextFieldGenero.getText().trim());
            
            jTextFieldDirector.setText("");
            jTextFieldGenero.setText("");
            jTextFieldTitulo.setText("");
            jTextFieldDuracion.setText("");
            jTextFieldAnho.setText("");
            
            PeliculaController controlador = new PeliculaController();
            try {
                controlador.agregarPeliculaController(peliculaNueva, this.conector.getConn());
            } catch (SQLException ex) {
                Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
            }
            
                JOptionPane.showMessageDialog(this,"La Pelicula " +peliculaNueva.getTitulo()+ " fue agregada con exito");
        }
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

